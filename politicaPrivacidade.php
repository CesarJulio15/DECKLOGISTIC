    <h1>Política de Privacidade</h1>
    <p>A presente <strong class="claro">Política de Privacidade</strong> descreve como o <strong class="claro">DeckLogistic</strong> realiza a coleta, o uso, o armazenamento e a proteção das informações pessoais e empresariais de seus usuários, em conformidade com a <strong class="claro">Lei nº 13.709/2018 – Lei Geral de Proteção de Dados (LGPD)</strong>.</p>

    <h2>1. Coleta de Dados</h2>
    <p>O DeckLogistic coleta dados fornecidos diretamente pelo usuário no momento do cadastro e durante o uso do sistema. Os dados coletados podem incluir:</p>
    <ul>
        <li>Nome da empresa e nome fantasia;</li>
        <li>Razão social, CNPJ, NIR, inscrição estadual e CNAE;</li>
        <li>Endereço, CEP, município, UF e país;</li>
        <li>E-mail corporativo e telefone de contato;</li>
        <li>Informações de login (usuário e senha criptografada);</li>
        <li>Informações financeiras e contábeis cadastradas no sistema (como receitas, despesas, saldos, registros de lucros e movimentações);</li>
        <li>Informações de estoque (produtos, quantidades, valores e movimentações);</li>
        <li>Dados técnicos, como endereço IP, navegador, data e hora de acesso, quando necessário para segurança e auditoria.</li>
    </ul>

    <h2>2. Finalidade do Tratamento dos Dados</h2>
    <p>Os dados são tratados exclusivamente para fins legítimos relacionados ao funcionamento do sistema, incluindo:</p>
    <ul>
        <li>Permitir o cadastro e autenticação de usuários e empresas;</li>
        <li>Gerenciar e manter registros de estoque e finanças;</li>
        <li>Emitir relatórios e gráficos gerenciais;</li>
        <li>Garantir a segurança e integridade das informações cadastradas;</li>
        <li>Atender obrigações legais, fiscais e regulatórias, quando aplicável;</li>
        <li>Melhorar continuamente as funcionalidades e a experiência do usuário no sistema.</li>
    </ul>

    <h2>3. Base Legal para o Tratamento</h2>
    <p>O tratamento dos dados pessoais é realizado com base nas seguintes hipóteses legais previstas na LGPD:</p>
    <ul>
        <li><strong class="claro">Execução de contrato</strong> – quando necessário para o uso do sistema e suas funcionalidades;</li>
        <li><strong class="claro">Cumprimento de obrigação legal</strong> – quando houver exigência de armazenamento de informações fiscais ou contábeis;</li>
        <li><strong class="claro">Consentimento do titular</strong> – quando o usuário fornece voluntariamente seus dados ao cadastrar-se no sistema.</li>
    </ul>

    <h2>4. Armazenamento e Segurança</h2>
    <p>O DeckLogistic adota medidas técnicas e administrativas para proteger os dados contra acessos não autorizados, destruição, perda, alteração, comunicação ou difusão indevida, tais como:</p>
    <ul>
        <li>Criptografia de senhas e dados sensíveis;</li>
        <li>Conexões seguras (HTTPS);</li>
        <li>Backups periódicos e armazenamento protegido;</li>
        <li>Controle de acesso por níveis de permissão;</li>
        <li>Monitoramento de tentativas de login e acessos suspeitos.</li>
    </ul>

    <h2>5. Compartilhamento de Dados</h2>
    <p>Os dados coletados <strong class="claro">não são vendidos ou compartilhados</strong> com terceiros, exceto nas seguintes situações:</p>
    <ul>
        <li>Quando houver obrigação legal, judicial ou regulatória;</li>
        <li>Quando necessário para proteger os direitos e a segurança do usuário ou do sistema;</li>
        <li>Mediante consentimento explícito do usuário.</li>
    </ul>

    <h2>6. Direitos do Titular dos Dados</h2>
    <p>Nos termos da LGPD, o usuário tem direito de:</p>
    <ul>
        <li>Solicitar confirmação da existência de tratamento de dados pessoais;</li>
        <li>Requerer acesso, correção, exclusão ou anonimização dos dados;</li>
        <li>Solicitar portabilidade dos dados a outro serviço;</li>
        <li>Revogar o consentimento a qualquer momento;</li>
        <li>Obter informações sobre o compartilhamento de dados com terceiros.</li>
    </ul>
    <p>As solicitações podem ser realizadas por meio do e-mail: <strong class="claro">privacidade@decklogistic.com.br</strong>.</p>

    <h2>7. Retenção dos Dados</h2>
    <p>Os dados serão mantidos pelo período necessário para cumprir as finalidades descritas nesta política, obrigações legais ou até que o usuário solicite a exclusão de sua conta. Após a exclusão, os dados poderão ser anonimizados para fins estatísticos.</p>

    <h2>8. Alterações nesta Política</h2>
    <p>Esta Política de Privacidade poderá ser atualizada periodicamente. As alterações serão informadas no sistema ou por e-mail aos usuários cadastrados.</p>

    <h2>9. Contato</h2>
    <p>Em caso de dúvidas, solicitações ou reclamações sobre privacidade e proteção de dados, entre em contato conosco:</p>
